                    <h4 class="text-warning">1) Cloth Cutting</h4>
                    <p class="p-10 ta-justify">
                        First the cloth according to the design. Since Bandhani is a textile pattern one can make
                        Panjabi Dress as well as Saree from bandhani. So the cloth is cut according to the kind of
                        the
                        dress it will be. The person who cuts the cloth is the one who transfers design on the
                        cloth.
                        Hence after cutting he sits to transfer the design through Farma.
                    </p>
                    <h4 class="text-warning">2) Farma</h4>
                    <p class="p-10 ta-justify">
                        Farma is a transparent plastic sheet, on which the design is created first. This design is
                        created either by Professional Designer or by Wholesaler or sometimes by the craft people
                        themselves. In this case, the craft person is the designer and he sells his design to the
                        wholesaler. Once the design is approved they create small pin holes in the plastic sheet.
                        With
                        the help of these pin holes Design is transferred on the cloth.</p>
                    <h4 class="text-warning">3) Transfering Design on Cloth</h4>
                    <p class="p-10 ta-justify">
                        Once the Farma is ready , thebundle of the cut cloth is laid on the ground. Then the Farma
                        is
                        put on top of it. Then with the help of the colour water and Squeeze the design is
                        transferred
                        on cloth. Since the size of the cloth is big, for good transfer, this process requires two
                        people to do it. Once the Design transfer is done this person sends this transferred cloth
                        to
                        the wholesaler who, later Distributes to the ladies who during their free time do the
                        “Bandhej”
                        work.
                    </p>
