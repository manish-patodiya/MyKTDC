                    <h4 class="text-warning">6) Bandhani Dying </h4>
                    <p class="p-10 ta-justify">
                        After Bandhej for Colour is done it goes for colour. It is not necessary that every piece of
                        design have Bandhej for Colour. Some might have only design Bandhej. This kind of cloth
                        directly
                        goes for colour. Again the person who has designed it decides colours. Once the colour is
                        made
                        then they dip cloths into it for around 1 -1.5 minutes per cloth. Then it goes for drying.
                        This
                        process is used when base colour is light and the colour, which will now be added, is dark.
                        If
                        it is other way round they have to do another process, which is called ‘wet colouring’. In
                        that
                        materials are same but it is done on the stove. After colour it goes for Drying or for
                        removing
                        Bandhej.
                    </p>
