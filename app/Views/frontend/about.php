                    <h4 class="text-warning fw-500 fs-22 mt-40 mb-30">Shekhawati: Tie and Dye Craft</h4>
                    <p class="ta-justify">
                        The textile industry in India has a colourful history consisting of a diverse
                        range of techniques and traditions. Tie and dye, or bandhani, is derived
                        from Sanskrit word 'bandh", meaning 'to tie' and is one of the most
                        popular and widespread textile arts of India extending across the country
                        from the states of Gujarat and Rajasthan. Bandhani, in fact, is the name
                        given to the characteristic feature on Indian tie and dye. It refers to the
                        small dot found in the centre of square or circle motifs, caused by the
                        penetration of the dye. The lack of this dot indicates that a tie and dye
                        item was mechanically-printed and not produced using traditional Indian
                        techniques.
                    </p>
                    <p class="ta-justify">
                        A symbol of the identity of the Shekhawati region, the Bundi-Bandhej work
                        is a sign of rich cultural heritage and respect along with the source of
                        income-earning of artisans. From the colorful chunari, a symbol of good
                        fortune and suhag, to the turban, which shows pride and self-respect, this
                        business has been on the progress path from generation to generation.
                        The majority of whose artisans are women.
                    </p>
                    <p class="ta-justify">
                        शेखावाटी क्षेत्र की पहचान का प्रतीक, बूंदी-बंधेज कार्य हुनरबाजो के आय-अर्जन स्रोत के साथ ही
                        समृद्ध सांस्कृतिक धरोहर एवं सम्मान का परिचायक है. रंग-बिरंगी सौभाग्य एवं सुहाग की निशानी चुनरी
                        से लेकर आन-बान-शान व स्वाभिमान को प्रकट करती पगड़ी तक यह कारोबार कालान्तर से पीढ़ी-दर-पीढ़ी
                        प्रगतिपथ पर रहा. जिसकी अधिसंख्य कारीगर महिला वर्ग है. </p>
