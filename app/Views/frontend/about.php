                    <h4 class="text-warning fw-500 fs-22 mt-40 mb-30">कोलसिया टाई एंड डाई कलस्टर की आवश्यकता –</h4>
                    <p class="ta-justify">इनसाइट स्टोरी
                        विगत आधी सदी के दौर में मशीनी प्रभाव तथा बाजार के बदलते स्वरुप ने अधिक हाथों को काम देने वाले
                        बूंदी-बंधेज कार्य दिशा को बदला. जिस कारण से वास्तविक कारीगर को पूरा लाभ नहीं मिलने से फाका-कस्सी
                        की और अग्रसर होने लगे है. साथ ही असंगठित कारीगर अत्यल्प आमदनी पर मजदूर बनकर काम करने को मजबूर
                        होने लगे है. लिहाजा शेखावाटी क्षेत्र में बूंदी-बंधेज कार्य में लगे लोगों का जोश कम हो रहा है.
                    </p>
                    <p class="ta-justify">
                        During the last half century, the mechanical effect and the changing nature of the market
                        changed the direction of Bundi-Bandhej work giving more hands to work. Due to which the genuine
                        artisans are not getting the full benefits, they have started moving towards faka-kassi. Along
                        with this, the unorganized artisans are being forced to work as laborers on meager income.
                        Therefore, the enthusiasm of the people engaged in Bundi-Bandhej work in Shekhawati region is
                        decreasing.
                    </p>
                    <p class="ta-justify">
                        कमलनिष्ठा संस्थान, कोलसिया ने एक सर्वेक्षण के माध्यम से स्थिति को अनुभव कर बूंदी-बंधेज
                        कार्यक्षेत्र
                        की कठिनाइयों को समझा तथा दिन-प्रतिदिन भूगर्भ जल की सम्भावित समस्या में समाप्त होते जल आधारित
                        कारोबारों के स्थान पर शेखावाटी में दस्तकारी को सशक्त करने के लिए “कोलसिया टाई एंड डाई कलस्टर” की
                        संकल्पना की, जिसे सूक्ष्म, लघु एवं मध्यम उद्यम मंत्रालय, भारत सरकार, नई दिल्ली की स्फूर्ति योजना
                        में
                        मूर्त रूप दिया जा सका. </p>
                    <p class="ta-justify">
                        Kamalnishtha Sansthan, Kolasiya, after experiencing the situation through a survey, understood
                        the
                        difficulties of the Bundi-Bandhej area and to strengthen the handicrafts in Shekhawati in place
                        of
                        water based businesses ending in the possible problem of ground water day by day. Concept of
                        “Tie
                        and Die Cluster” which could be materialized in SFURTI scheme of Ministry of Micro, Small and
                        Medium
                        Enterprises, Government of India, New Delhi.
                    </p>
                    <p class="ta-justify">
                        आरम्भकाल में कोलसिया तथा इसके आसपास के ईलाके के आठ गाँव की 349 बूंदी-बंधेज महिला कारीगरों को
                        लक्ष्य
                        में शामिल किया गया है. जिसे आगामी काल में सम्पूर्ण शेखावाटी, झुंझुनू, सीकर एवं चुरू जिले की
                        अनेकानेक
                        कारीगर महिलाओ का शक्ति केंद्र बनाना तस्सव्वुर में है. इस कलस्टर के माध्यम से शेखावाटी हस्तशिल्प
                        कला
                        को अंतर्राष्ट्रीय स्तर तक ले जाने की मूल संकल्पन्ना है. जिससे आने समय में शेखावाटी क्षेत्र
                        हस्तशिल्प
                        का निर्यात-जोन बन सके.
                    </p>
                    <p class="ta-justify">
                        Initially, 349 Bundi-Bandhej women artisans from eight villages of Kolasiya and its surrounding
                        areas have been included in the target. In the coming period, it is in Tassavvur to make power
                        center of many artisan women of entire Shekhawati, Jhunjhunu, Sikar and Churu districts. The
                        basic
                        concept is to take Shekhawati handicraft art to the international level through this cluster. So
                        that Shekhawati region can become an export-zone of handicrafts in the coming time.
                    </p>
